#include "bitstream/bitstream.h"
#include "archiver/archiver.h"
#include "unarchiver/unarchiver.h"
#include <stdio.h>
#include <stdlib.h>

enum { BITS_IN_BYTE = 8 };

int main(void) {
    char operation;
    TBitStream fileIn = {
        .Buffer = 0x00,
        .Count = BITS_IN_BYTE,
        .Stream = fopen("in.txt", "rb")
    };
    TBitStream fileOut = {
        .Buffer = 0x00,
        .Count = 0,
        .Stream = fopen("out.txt", "wb")
    };
    int isEOF = fread(&operation, sizeof(char), 1, fileIn.Stream);
    if (isEOF == EOF) {
        printf("bad input");
        return EXIT_SUCCESS;
    }
    if (operation == 'c') {
        Archiver(/*inputFile*/&fileIn, /*outputFile*/&fileOut);
    } else if (operation == 'd') {
        Unarchiver(/*inputFile*/&fileIn, /*outputFile*/&fileOut);
    } else {
        printf("Please, enter the correct opertion\
                \n(c - archive, d - unarchive) and try again;)");
    }
    int isErorInInput = fclose(fileIn.Stream);
    int isErorInOutput = CloseBitStream(/*file*/&fileOut);
    if ((isErorInInput < 0) || (isErorInOutput < 0)) {
        printf("error\n");
    }
    return EXIT_SUCCESS;
}

